//  ! Primitive Data Types

//  Number - Storing age
const age = 25;
console.log(age);

//  String - Storing name
const name = "Krishna";
console.log(name);

//  Boolean - Storing switch
const isSwitch = false;
console.log(isSwitch);

// Undefined - A variable that has been declared but not yet assigned
let numberOfChildren;
console.log(numberOfChildren);

// NaN/null intentionally setting a variable to have no value

const salary = null;
console.log(salary);

//  ! Non Primitive Data Types

// object - storing Data Types

const user = {
  firstName: "Krishna",
  lastName: "Ruparelia",
  age: 23,
};
console.log(user);

