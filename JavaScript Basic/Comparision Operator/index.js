// !  Define the Variables

let ageOfJohn = 25; // Number
let ageOfDoe = 34;
let UserInput = "25"; // String
let userOutput = 25; // Number

//  Using == (double equal to)

console.log(ageOfJohn == UserInput); // true

//  Using === (thriple equal to)

console.log(ageOfJohn === UserInput); // false

console.log(ageOfJohn === userOutput); // true

// Not Equal to '!='

console.log(ageOfJohn != UserInput);

// Strict Not Equal to '!=='

console.log(ageOfJohn !== UserInput);

// using >

console.log(ageOfJohn > ageOfDoe);

// using <

console.log(ageOfJohn < ageOfDoe);

//using >=

console.log(ageOfJohn >= ageOfDoe);
console.log(ageOfJohn >= userOutput);

//using <=

console.log(ageOfJohn <= ageOfDoe);
console.log(ageOfJohn <= userOutput);
