// Push & UnShift

// using push method

const fruited = ["Apple", "Banana"];
console.log(fruited);
const newArrayofFruits = fruited.push("kiwi", "Mango");
console.log(newArrayofFruits);
console.log(fruited);

// using unShift

const names = [12, 12, 22, 12];
console.log(names);
const newArrayNumbers = names.unshift(1, 4);
console.log(newArrayNumbers);
console.log(names);
