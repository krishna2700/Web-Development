// Multi-Dimensional Arrays

//  creating a 2D array
const matrix = [
  [1, 2, 3],
  [4, 5, 6],
  [7, 8, 9],
];

//  Access the elements
console.log(matrix);
console.log(matrix[0]);
console.log(matrix[0][0]);
console.log(matrix[1]);
console.log(matrix[1][0]);
console.log(matrix[2]);
console.log(matrix[2][2]);

//  cretaing a 3D array

const threeDArray = [
  [
    [1, 2],
    [3, 4],
  ],
  [
    [5, 6],
    [7, 8],
  ],
];

// Access the elements
console.log(threeDArray);
console.log(threeDArray[0]);
console.log(threeDArray[0][0][0]);
console.log(threeDArray[0][1][1]);
console.log(threeDArray[1]);
console.log(threeDArray[1][1][1]);
console.log(threeDArray[1][0][1]);
